window.fluid = {
    setTheme: (themeName) => {
        if (THEMES[themeName]) {
            currentTheme = themeName;
        }
    },
    triggerSplats: (amount, color, x, y) => {
        // color is optional {r, g, b} (0-1)
        // x, y are optional 0-1 coordinates
        for (let i = 0; i < amount; i++) {
            const c = color ? { ...color } : THEMES[currentTheme].generate();
            // boosted intensity for splats
            if (color) { c.r *= 10; c.g *= 10; c.b *= 10; }

            const px = x !== undefined ? x : Math.random();
            const py = y !== undefined ? y : Math.random();

            // Variance in strength/size
            const forceVar = 1.0 + (Math.random() * 0.5 - 0.25); // +/- 25%
            const dx = 1000 * (Math.random() - 0.5) * forceVar;
            const dy = 1000 * (Math.random() - 0.5) * forceVar;

            // Pass force scaling to splat? splat uses global config.SPLAT_RADIUS.
            // We can hack radius by scaling it in the uniform if we update splat signature,
            // or just rely on force (dx/dy) for "strength".
            splat(px, py, dx, dy, c);
        }
    },
    triggerComplexSplat: (colors, config = {}) => {
        // config: { startX, startY, dirX, dirY, forceScale }
        const centerX = config.startX !== undefined ? config.startX : 0.5;
        const centerY = config.startY !== undefined ? config.startY : 0.8;

        // Directional bias?
        const dirX = config.dirX !== undefined ? config.dirX : 0;
        const dirY = config.dirY !== undefined ? config.dirY : 0;
        const forceScale = config.forceScale !== undefined ? config.forceScale : 1.0;

        for (let i = 0; i < colors.length; i++) {
            // Weaker intensity than single triggerSplats
            const c = { ...colors[i] };
            // Base multiplier was 5, apply forceScale
            c.r *= 5 * forceScale; c.g *= 5 * forceScale; c.b *= 5 * forceScale;

            // 5 splats per color component (Optimized from 15 to prevent freeze)
            for (let j = 0; j < 5; j++) {
                const px = centerX + (Math.random() - 0.5) * 0.2; // Cluster spread
                const py = centerY + (Math.random() - 0.5) * 0.2;

                // Velocity: Mix of random spread + directional bias
                // Random component
                let dx = 500 * (Math.random() - 0.5);
                let dy = 500 * (Math.random() - 0.5);

                // Add directional bias (strong push)
                if (dirX || dirY) {
                    dx += dirX * 2000;
                    dy += dirY * 2000;
                }

                dx *= forceScale;
                dy *= forceScale;

                splat(px, py, dx, dy, c);
            }
        }
    }
};

