function spawnAmbientParticle() {
    if (ambientParticles.length > 20) return; // Cap performance

    const lifetime = Math.random() * ((config.AMBIENT_LIFETIME_MAX || 3000) - (config.AMBIENT_LIFETIME_MIN || 1000)) + (config.AMBIENT_LIFETIME_MIN || 1000);

    // Random Start Position
    const x = Math.random();
    const y = Math.random();

    // Initial Velocity (Random direction)
    const force = config.AMBIENT_FORCE !== undefined ? config.AMBIENT_FORCE : 600;
    const dx = force * (Math.random() - 0.5);
    const dy = force * (Math.random() - 0.5);

    // Acceleration (Curving behavior)
    // Random acceleration vector to pull it in a curve
    const ax = 600 * (Math.random() - 0.5);
    const ay = 600 * (Math.random() - 0.5);

    // Generate Start and End colors for interpolation
    const startColor = THEMES[currentTheme].generate();
    const endColor = THEMES[currentTheme].generate();

    ambientParticles.push({
        x, y,
        dx, dy,
        ax, ay,
        startColor,
        endColor,
        age: 0,
        lifetime: lifetime / 1000 // Convert to seconds for dt
    });
}

function updateAmbientParticles(dt) {
    for (let i = ambientParticles.length - 1; i >= 0; i--) {
        let p = ambientParticles[i];
        p.age += dt;

        if (p.age >= p.lifetime) {
            ambientParticles.splice(i, 1);
            continue;
        }

        // Apply Acceleration to Velocity
        p.dx += p.ax * dt;
        p.dy += p.ay * dt;

        // Apply Velocity to Position (Approximate for Splat)
        // Note: Splat takes 0-1 coord, but we are moving a "source"
        // We simulate movement by just splatting at the new location?
        // Actually, we usually drag the source.
        // Let's move the x/y slightly based on velocity normalized to screen space
        // dx is in "force units" (pixels/s approx or huge numbers).
        // Let's just move x/y by a small fraction of dx/dy to simulate source movement.
        p.x += (p.dx * 0.00005) * dt * 60;
        p.y += (p.dy * 0.00005) * dt * 60;

        // Bounce off walls? Repulsion field.
        const margin = config.AMBIENT_REPULSION_MARGIN || 0.15;
        const repulsionForce = config.AMBIENT_REPULSION || 2000;

        // X Axis Repulsion
        if (p.x < margin) {
            // Force pushes Right (+). Stronger as dist -> 0
            let dist = p.x;
            let strength = (1.0 - dist / margin); // 0 at margin, 1 at edge
            p.dx += repulsionForce * (strength * strength) * dt;
        } else if (p.x > 1.0 - margin) {
            // Force pushes Left (-)
            let dist = 1.0 - p.x;
            let strength = (1.0 - dist / margin);
            p.dx -= repulsionForce * (strength * strength) * dt;
        }

        // Y Axis Repulsion
        if (p.y < margin) {
            // Force pushes Down (+)
            let dist = p.y;
            let strength = (1.0 - dist / margin);
            p.dy += repulsionForce * (strength * strength) * dt;
        } else if (p.y > 1.0 - margin) {
            // Force pushes Up (-)
            let dist = 1.0 - p.y;
            let strength = (1.0 - dist / margin);
            p.dy -= repulsionForce * (strength * strength) * dt;
        }

        // Interpolate Color: Start -> End based on age
        const t = p.age / p.lifetime; // 0.0 to 1.0

        const r = p.startColor.r + (p.endColor.r - p.startColor.r) * t;
        const g = p.startColor.g + (p.endColor.g - p.startColor.g) * t;
        const b = p.startColor.b + (p.endColor.b - p.startColor.b) * t;

        const color = { r, g, b };

        if (config.AMBIENT_BLOOM) {
            color.r *= config.AMBIENT_BLOOM;
            color.g *= config.AMBIENT_BLOOM;
            color.b *= config.AMBIENT_BLOOM;
        }

        // We use the current velocity (dx, dy) for the splat force
        splat(p.x, p.y, p.dx, p.dy, color, config.AMBIENT_RADIUS / 100.0);
    }
}


